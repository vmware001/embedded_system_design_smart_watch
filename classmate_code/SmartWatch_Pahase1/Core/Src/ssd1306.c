/* ======== 底层 I2C 收发 ======== */

void SSD1306_WriteCommand(uint8_t command) {
    while (HAL_I2C_GetState(&hi2c1) != HAL_I2C_STATE_READY);
    HAL_I2C_Mem_Write(&hi2c1, SSD1306_I2C_ADDR, 0x00, I2C_MEMADD_SIZE_8BIT, &command, 1, HAL_MAX_DELAY);
}

void SSD1306_WriteData(uint8_t* data, uint16_t size) {
    while (HAL_I2C_GetState(&hi2c1) != HAL_I2C_STATE_READY);
    HAL_I2C_Mem_Write(&hi2c1, SSD1306_I2C_ADDR, 0x40, I2C_MEMADD_SIZE_8BIT, data, size, HAL_MAX_DELAY);
}

/* ======== 初始化 ======== */

void SSD1306_Init(void) {
    HAL_Delay(100);

    SSD1306_WriteCommand(0xAE); // 关闭显示
    SSD1306_WriteCommand(0x20); // 设置寻址模式
    SSD1306_WriteCommand(0x00); // 水平寻址模式 (便于整屏刷写)
    SSD1306_WriteCommand(0x21); // 设置列地址范围
    SSD1306_WriteCommand(0x00); // 起始列 0
    SSD1306_WriteCommand(0x7F); // 结束列 127
    SSD1306_WriteCommand(0x22); // 设置页地址范围
    SSD1306_WriteCommand(0x00); // 起始页 0
    SSD1306_WriteCommand(0x07); // 结束页 7
    SSD1306_WriteCommand(0xA1); // 左右翻转映射
    SSD1306_WriteCommand(0xC8); // 上下翻转扫描
    SSD1306_WriteCommand(0xDA); // COM引脚配置
    SSD1306_WriteCommand(0x12); // 128x64面板适配
    SSD1306_WriteCommand(0x81); // 对比度
    SSD1306_WriteCommand(0xFF); // 亮度最大
    SSD1306_WriteCommand(0xA6); // 正常显示
    SSD1306_WriteCommand(0xA8); // 复用率
    SSD1306_WriteCommand(0x3F); // 64行
    SSD1306_WriteCommand(0xD3); // 显示偏移
    SSD1306_WriteCommand(0x00);
    SSD1306_WriteCommand(0x40); // 起始行
    SSD1306_WriteCommand(0xD5); // 时钟分频
    SSD1306_WriteCommand(0x80);
    SSD1306_WriteCommand(0x8D); // 电荷泵
    SSD1306_WriteCommand(0x14); // 开启电荷泵
    SSD1306_WriteCommand(0x2E); // 禁用滚动
    SSD1306_WriteCommand(0xAF); // 开启显示
    HAL_Delay(50);

    /* 清空显存 */
    SSD1306_Clear();
    SSD1306_Flush();
}

/* ======== 全屏点亮（硬件测试用） ======== */

void SSD1306_FillAll(void) {
    for (int i = 0; i < 1024; i++) framebuffer[i] = 0xFF;
    SSD1306_Flush();
}

/* ======== 清屏 (仅清显存缓冲) ======== */

void SSD1306_Clear(void) {
    for (int i = 0; i < 1024; i++) framebuffer[i] = 0x00;
    cursor_x = 0;
    cursor_page = 0;
}

/* ======== 将显存一次性刷到屏幕 ======== */

void SSD1306_Flush(void) {
    while (HAL_I2C_GetState(&hi2c1) != HAL_I2C_STATE_READY);
    HAL_I2C_Mem_Write(&hi2c1, SSD1306_I2C_ADDR, 0x40, I2C_MEMADD_SIZE_8BIT,
                      framebuffer, 1024, HAL_MAX_DELAY);
}

/* ======== 设置光标位置 ======== */

void SSD1306_SetCursor(uint8_t x, uint8_t page) {
    if (x > 127) x = 127;
    if (page > 7) page = 7;
    cursor_x = x;
    cursor_page = page;
}

/* ======== 写入单个字符 (写入显存缓冲) ======== */

void SSD1306_WriteChar(char ch) {
    if (ch < 32 || ch > 126) ch = ' ';
    uint8_t idx = ch - 32;

    /* 将 6 列点阵写入显存 */
    for (uint8_t i = 0; i < 6; i++) {
        if (cursor_x + i < 128) {
            framebuffer[cursor_page * 128 + cursor_x + i] = Font_6x8[idx][i];
        }
    }

    /* 光标右移 6 像素 */
    cursor_x += 6;
    if (cursor_x > 122) {
        cursor_x = 0;
        if (cursor_page < 7) cursor_page++;
    }
}

/* ======== 写入字符串 ======== */

void SSD1306_WriteString(char *str) {
    while (*str) {
        SSD1306_WriteChar(*str);
        str++;
    }
}

/* ======== 设置单个像素 (内部辅助) ======== */
static void fb_set_pixel(uint8_t x, uint8_t y)
{
    if (x >= 128 || y >= 64) return;
    framebuffer[(y / 8) * 128 + x] |= (1UL << (y % 8));
}

/* ======== 绘制缩放字符: (x,y) 像素坐标, sx/sy 为横纵缩放倍数 ======== */
void SSD1306_DrawBigChar(char ch, uint8_t x, uint8_t y, uint8_t sx, uint8_t sy)
{
    if (ch < 32 || ch > 126) ch = ' ';
    uint8_t idx = ch - 32;

    for (uint8_t c = 0; c < 6; c++) {
        uint8_t col = Font_6x8[idx][c];
        for (uint8_t r = 0; r < 8; r++) {
            if (col & (1UL << r)) {
                for (uint8_t dx = 0; dx < sx; dx++) {
                    for (uint8_t dy = 0; dy < sy; dy++) {
                        fb_set_pixel(x + c * sx + dx, y + r * sy + dy);
                    }
                }
            }
        }
    }
}

/* ======== 绘制空心矩形 ======== */
void SSD1306_DrawRect(uint8_t x, uint8_t y, uint8_t w, uint8_t h)
{
    uint8_t i;
    /* 上下边 */
    for (i = 0; i < w; i++) {
        fb_set_pixel(x + i, y);
        if (h > 1) fb_set_pixel(x + i, y + h - 1);
    }
    /* 左右边 */
    for (i = 1; i < h - 1; i++) {
        fb_set_pixel(x, y + i);
        if (w > 1) fb_set_pixel(x + w - 1, y + i);
    }
}

