/* UTF-8 查找表: 与 CN_Font16 顺序一一对应, 每个汉字3字节 */
static const char CN_Lookup[] = "春嵌入式课设刘缘园王昊柳政道";

/* ======== 在查找表中定位汉字, 返回索引, 未找到返回 -1 ======== */
static int CN_LookupIndex(const uint8_t *utf8)
{
    uint8_t i;
    for (i = 0; i < sizeof(CN_Lookup) - 1; i += 3) {
        if (utf8[0] == (uint8_t)CN_Lookup[i] &&
            utf8[1] == (uint8_t)CN_Lookup[i + 1] &&
            utf8[2] == (uint8_t)CN_Lookup[i + 2]) {
            return i / 3;
        }
    }
    return -1;
}

