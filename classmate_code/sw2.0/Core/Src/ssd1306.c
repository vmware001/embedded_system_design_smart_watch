// 外部声明 I2C 句柄（根据实际使用的 I2C 接口修改，如 hi2c2）
extern I2C_HandleTypeDef hi2c1;

// SSD1306 的 I2C 写地址 = 0x3C << 1 = 0x78
#define SSD1306_I2C_ADDR    (SSD1306_ADDR << 1)

// 控制字节定义
#define SSD1306_CTRL_CMD    0x00    // Co=0, D/C#=0: 后续为命令
#define SSD1306_CTRL_DATA   0x40    // Co=0, D/C#=1: 后续为数据

/**
 * @brief 向 SSD1306 发送控制字节 + 负载（命令或数据）
 * @param ctrl 控制字节：0x00 表示命令，0x40 表示数据
 * @param payload 实际发送的字节
 */
// 引用 freertos.c 创建的互斥锁句柄
extern osMutexId mutexI2CHandle;

static void SSD1306_SendByte(uint8_t ctrl, uint8_t payload)
{
    uint8_t buf[2] = {ctrl, payload};

    osMutexWait(mutexI2CHandle, osWaitForever);

    HAL_I2C_Master_Transmit(&hi2c1, SSD1306_I2C_ADDR, buf, 2, HAL_MAX_DELAY);

    osMutexRelease(mutexI2CHandle);
}

/**
 * @brief 发送单条命令
 */
static void SSD1306_WriteCommand(uint8_t cmd)
{
    SSD1306_SendByte(SSD1306_CTRL_CMD, cmd);
}

/**
 * @brief 发送单个数据字节
 */
static void SSD1306_WriteData(uint8_t data)
{
    SSD1306_SendByte(SSD1306_CTRL_DATA, data);
}

